int		ft_atoi(char *str)
{
	int	i;
	int	res;
	int	sign;

	if (str[0] == '-')
	{
		sign = -1;
		i = 1;
	}
	else
	{
		sign = 1;
		i = 0;
	}
	res = 0;
	while (str[i] != '\0' && str[i] < 10 && str[i] >= 0)
	{
		res = res * 10 + str[i];
		i++;
	}
	res = res * sign;
	return (res);
}
