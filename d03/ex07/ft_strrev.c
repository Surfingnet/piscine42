char	*ft_strrev(char *str)
{
	int		len;
	char	*rev;
	int		i;

	len = 0;
	while (str[len] != '\0')
	{
		len++;
	}
	i = 0;
	while (i < len)
	{
		rev[i] = str[len - i - 1];
		i++;
	}
	return (*rev);
}
