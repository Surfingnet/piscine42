void ft_print_comb2(void)
{
	int i, m, c, d, u;

	i = 1;
	while (i < 9900)
	{
		m = (i / 1000);
		c = ((i - (1000 * m)) / 100);
		d = ((i - (1000 * m) - (100 * c)) / 10);
		u = (i - (1000 * m) - (100 * c) - (d * 10));
		if ((u + (10 * d)) > (c + (10 * m)))
		{
			ft_putchar (m + '0');
			ft_putchar (c + '0');
			ft_putchar (' ');
			ft_putchar (d + '0');
			ft_putchar (u + '0');
			if (i < 9899)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
		}
		i++;
	}
}
