int	ft_sqrt(int nb)
{
	int res;
	int pow2;

	if (nb < 1)
		return (0);
	res = 1;
	pow2 = 1;
	while (pow2 < nb)
	{
		res++;
		pow2 = res * res;
	}
	return (pow2 == nb ? res : 0);
}
